    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String username = txtUsername.getText();
    String password = String.valueOf(txtPassword.getPassword());
     
    if (!username.isEmpty() && !password.isEmpty()) {
        try {
            Statement stmt = (Statement) Connect.configDB().createStatement();
            
            // Cek di tabel login (untuk admin dan kasir)
            String queryLogin = "SELECT * FROM login WHERE username = '" + username + "'";
            ResultSet rsLogin = stmt.executeQuery(queryLogin);
            
            if (rsLogin.next()) {
                // User ditemukan di tabel login
                if (password.equals(rsLogin.getString("password"))) {
                    if (username.equals("admin")) {
                        adminDashboard adm = new adminDashboard();
                        adm.setVisible(true);
                        clear();
                        this.dispose();
                    } else if (username.contains("kasir")) {
                        kasirDashboard kd = new kasirDashboard();
                        kd.setVisible(true);
                        clear();
                        this.dispose();
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Wrong Password");
                    clear();
                }
            } else {
                // Jika tidak ditemukan di login, cek di tabel customer
                String queryCustomer = "SELECT * FROM customer WHERE username = '" + username + "'";
                ResultSet rsCustomer = stmt.executeQuery(queryCustomer);
                
                if (rsCustomer.next()) {
                    // User ditemukan di tabel customer
                    if (password.equals(rsCustomer.getString("password"))) {
                        memberDashboard dashboard = new memberDashboard();
                        dashboard.labelNama.setText(rsCustomer.getString("nama").toUpperCase());
                        dashboard.setVisible(true);
                        dashboard.setLabelUsername(username);
                        clear();
                        this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(this, "Wrong Password");
                        clear();
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Username Not Found");
                    clear();
                }
                rsCustomer.close();
            }
            
            rsLogin.close();
            stmt.close();
            
        } catch (Exception e) {
            System.err.println(e);
        }
    } else {
        JOptionPane.showMessageDialog(this, "Please fill out the form");
    }
    }//GEN-LAST:event_btnLoginActionPerformed

